package lista6;

public class PratoEntrada {
	public int numero;
	public String nome;
	public String descricao;
	public double precoUnitario;
	
	public PratoEntrada(int numero, String nome, String descricao, double precoUnitario) {
		super();
		this.numero = numero;
		this.nome = nome;
		this.descricao = descricao;
		this.precoUnitario = precoUnitario;
	}
	
	
}
